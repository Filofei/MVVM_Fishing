import Foundation

/// An enum representing names of the locations on the bases.

enum Locations {
    enum Country {
        static let locations = ["Заросли", "Березки"]
    }
    enum MoscowRiver {
        static let locations = ["Ясный день", "Лёгкая рябь"]
    }
    enum Platnik {
        static let locations = ["Синяя гладь", "У развилки"]
    }
    enum Ob {
        static let locations = ["Тёмная вода", "Скалы"]
    }
}
